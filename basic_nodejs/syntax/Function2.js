//입력값을 받기 위한 매개변수(parameter) 선언.
function sum(first, second){ 
    console.log(first+second);
}
sum(2,4); //2와 4의 값은 first, second로 전달 == argument(인자)

//*함수라는 것은 입력값을 받을 수 있고 그에 따라 다르게 동작할 수 있음..
// 함수에 입력하는 값은 argument(인자)라고 부르고, 
// 인자를 받아서 함수 내로 전달하는 매개체를 parameter(매개변수)라고 한다..




