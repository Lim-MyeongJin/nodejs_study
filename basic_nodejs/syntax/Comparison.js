//비교(comparison) 연산자
// - 좌항과 우항을 비교해서 true 또는 false를 만들어내는 연산자
// - 조건문에서 사용되는 연산자
console.log(1==1); //true
console.log(1==2); //false
console.log(1>2);
console.log(1<2);

//정확하게 비교
console.log(1===1); //true 
console.log(1===2); //false

//헷갈릴 수 있는 부분
name = 1; //대입연산자
name == 1; //비교연산자
